/// 8×8 bitmap font for printable ASCII. Index: char_code * 8 + row (MSB = leftmost pixel).
/// Only the characters used in the hover overlay are non-zero.
pub fn make_font() -> [u32; 1024] {
    let mut f = [0u32; 1024];
    let mut g = |ch: u8, rows: [u8; 8]| {
        let base = ch as usize * 8;
        for (i, &b) in rows.iter().enumerate() {
            f[base + i] = b as u32;
        }
    };

    g(b' ', [0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]);
    g(b'(', [0x1C, 0x30, 0x60, 0x60, 0x60, 0x30, 0x1C, 0x00]);
    g(b')', [0x38, 0x0C, 0x06, 0x06, 0x06, 0x0C, 0x38, 0x00]);
    g(b'+', [0x00, 0x18, 0x18, 0x7E, 0x18, 0x18, 0x00, 0x00]);
    g(b',', [0x00, 0x00, 0x00, 0x18, 0x18, 0x08, 0x10, 0x00]);
    g(b'-', [0x00, 0x00, 0x00, 0x7E, 0x00, 0x00, 0x00, 0x00]);
    g(b'.', [0x00, 0x00, 0x00, 0x00, 0x00, 0x18, 0x18, 0x00]);
    g(b'0', [0x3C, 0x66, 0x6E, 0x76, 0x66, 0x66, 0x3C, 0x00]);
    g(b'1', [0x18, 0x38, 0x18, 0x18, 0x18, 0x18, 0x7E, 0x00]);
    g(b'2', [0x3C, 0x66, 0x06, 0x0C, 0x18, 0x30, 0x7E, 0x00]);
    g(b'3', [0x3C, 0x66, 0x06, 0x1C, 0x06, 0x66, 0x3C, 0x00]);
    g(b'4', [0x0C, 0x1C, 0x3C, 0x6C, 0x7E, 0x0C, 0x0C, 0x00]);
    g(b'5', [0x7E, 0x60, 0x7C, 0x06, 0x06, 0x66, 0x3C, 0x00]);
    g(b'6', [0x1C, 0x30, 0x60, 0x7C, 0x66, 0x66, 0x3C, 0x00]);
    g(b'7', [0x7E, 0x06, 0x06, 0x0C, 0x18, 0x18, 0x18, 0x00]);
    g(b'8', [0x3C, 0x66, 0x66, 0x3C, 0x66, 0x66, 0x3C, 0x00]);
    g(b'9', [0x3C, 0x66, 0x66, 0x3E, 0x06, 0x0C, 0x38, 0x00]);
    g(b'=', [0x00, 0x00, 0x7E, 0x00, 0x7E, 0x00, 0x00, 0x00]);
    g(b'a', [0x00, 0x00, 0x3C, 0x06, 0x3E, 0x66, 0x3E, 0x00]);
    g(b'b', [0x60, 0x60, 0x7C, 0x66, 0x66, 0x66, 0x7C, 0x00]);
    g(b'd', [0x06, 0x06, 0x3E, 0x66, 0x66, 0x66, 0x3E, 0x00]);
    g(b'e', [0x00, 0x00, 0x3C, 0x66, 0x7E, 0x60, 0x3C, 0x00]);
    g(b'm', [0x00, 0x00, 0xEE, 0xCC, 0xCC, 0xCC, 0xCC, 0x00]);
    g(b'n', [0x00, 0x00, 0xE6, 0xC6, 0xC6, 0xC6, 0xC6, 0x00]);
    g(b'o', [0x00, 0x00, 0x38, 0x6C, 0xC6, 0x6C, 0x38, 0x00]);
    g(b'p', [0x00, 0x00, 0xFC, 0xC6, 0xFC, 0xC0, 0xC0, 0x00]);
    g(b's', [0x00, 0x00, 0x3C, 0x60, 0x3C, 0x06, 0x3C, 0x00]);
    g(b't', [0x18, 0x18, 0x7E, 0x18, 0x18, 0x18, 0x0E, 0x00]);
    g(b'w', [0x00, 0x00, 0xC3, 0xC3, 0xDB, 0xDB, 0x66, 0x00]);

    f
}
